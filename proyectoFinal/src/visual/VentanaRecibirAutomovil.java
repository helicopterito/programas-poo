    private void jBtnEntregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jBtnEntregarMouseClicked
        if(VentanaPrincipal.buscarAlquiler(Integer.parseInt(this.jTxtFieldNumeroAlquiler.getText()) - 1) != null)
        {
            VentanaPrincipal.buscarAutomovil(this.jTxtFieldPlaca.getText()).setDisponible(true);
            VentanaPrincipal.buscarAutomovil(this.jTxtFieldPlaca.getText()).setKilometraje(
            Integer.parseInt(this.jTxtFieldKilometraje.getText()));
            ListaDeAlquiler.LISTA_DE_ALQUILERES.get(Integer.parseInt(this.jTxtFieldNumeroAlquiler.getText()) - 1).setEstado(false);
            reestablecer();
            VentanaExito vExito = new VentanaExito();
        }
        else
        {
            this.jTxtFieldNumeroAlquiler.setText("Introduce valores válidos");
            this.jTxtFieldNumeroAlquiler.setForeground(Color.red);
            this.jTxtFieldNumeroCliente.setText("Introduce valores válidos");
            this.jTxtFieldNumeroCliente.setForeground(Color.red);
        }
    }//GEN-LAST:event_jBtnEntregarMouseClicked

